<h1>Projektziel</h1>
<div class="home-window"> 
<h2>Das Projektziel orientiert sich am Leitbild der Gustav-von-Schmoller-Schule anhand folgender konkreter Aussagen:</h2>
"Wir legen Wert auf Leistungsbereitschaft und ganzheitliches, eigenverantwortliches Lernen." <br>
Die Tutoren melden sich freiwillig bei ihren Fachlehrern, dabei wird die Auswahl der Tutoren über die Fachlehrer nach entsprechender Eignung, Fach- und Sozialkompetenz vorgenommen.<br> Durch das Erteilen von Nachhilfestunden wird die Eigenverantwortlichkeit, aber auch die Selbstständigkeit des nachhilfegebenden und des nachhilfeempfangenden Schülers gefördert, beide erwerben Kompetenzen, die sie auf ihre anschließende berufliche Tätigkeit vorbereiten.<br>
"Die Schulleitung initiiert und unterstützt Maßnahmen zur Verbesserung der Unterrichtsqualität. Die Schulleitung schafft die notwendigen Rahmenbedingungen für das Gelingen des Projekts,indem Räumlichkeiten, Bücher, Arbeitsmaterial usw. zur Verfügung gestellt werden."<br>
"Wir stärken Toleranz, Kritikfähigkeit, Verantwortungsbereitschaft und das soziale Miteinander auf der Basis eines demokratischen Grundverständnisses."
Stärkere Schüler unterstützen schwächere Schüler, sie zeigen somit Verantwortungsbereitschaft. Dadurch können sie ihre eigenen Stärken weiter ausbauen und gleichzeitig Schwächere beim Lernen unterstützen.
